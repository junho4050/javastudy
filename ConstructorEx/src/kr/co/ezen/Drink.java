package kr.co.ezen;

public class Drink {
    private String name;
    private int dosu;
    private String name2;
    
    
    
	public Drink(String name, int dosu, String name2) {
		super();
		this.name = name;
		this.dosu = dosu;
		this.name2 = name2;
		
		
		
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public int getDosu() {
		return dosu;
	}



	public void setDosu(int dosu) {
		this.dosu = dosu;
	}



	public String getName2() {
		return name2;
	}



	public void setName2(String name2) {
		this.name2 = name2;
	}
    


}
