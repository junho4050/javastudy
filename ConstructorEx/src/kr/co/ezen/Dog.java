package kr.co.ezen;

public class Dog {
	private String name;
	private String a;
	private int age;
	
	
	public Dog(String name, String a, int age) {
		super();
		this.name = name;
		this.a = a;
		this.age = age;
		
		
		
		
		
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getA() {
		return a;
	}


	public void setA(String a) {
		this.a = a;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}

}
